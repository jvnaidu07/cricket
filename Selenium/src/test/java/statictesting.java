public class statictesting {
      static void name(){
        System.out.println("kalyaan");
    }
    static int intr(){
        System.out.println(10);
        return 10;
    }
    static String val(){
        System.out.println("harish");
        return "harish";
    }

    public static void main(String[] args) {
         name();
        intr();
        val();
    }
}
