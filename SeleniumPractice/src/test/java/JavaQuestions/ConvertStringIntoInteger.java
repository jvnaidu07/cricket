package JavaQuestions;

public class ConvertStringIntoInteger {
    public static void main(String[] args) {
        //string to integer
        String a = "12345";
        int b = Integer.parseInt(a);
        System.out.println(b);


        //integer to string
        int c = 54321;
        String d = Integer.toString(c);
        System.out.println(d);



    }
}
